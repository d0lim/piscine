char	*ft_strcapitalize(char *str)
{
	int		count;

	count = 0;
	while (*str != '\0')
	{
		if (*str >= 97 && *str <= 122)
		{
			if (!(*(str - 1) >= 97 && *(str - 1) <= 122))
				if (!(*(str - 1) >= 65 && *(str - 1) <= 90))
					if (!(*(str - 1) >= 48 && *(str - 1) < 57))
						*str = *str - 32;
		}
		else if (*str >= 65 && *str <= 90)
			if ((*(str - 1) >= 97 && *(str - 1) <= 122) ||
			(*(str - 1) >= 65 && *(str - 1) <= 90) ||
			(*(str - 1) >= '0' && *(str - 1) <= '9'))
				*str = *str + 32;
		str++;
		count++;
	}
	return (str - count);
}
