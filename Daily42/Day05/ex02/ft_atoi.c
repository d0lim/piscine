int		ft_atoi(char *str)
{
	int		rt;
	int		flag;

	rt = 0;
	flag = 0;
	while (!(*str == '-' || *str == '+' || (*str >= '0' && *str <= '9')))
		str++;
	if (*str == '-')
	{
		str++;
		flag = 1;
	}
	else if (*str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		rt = rt * 10 + ((*str) - '0');
		str++;
	}
	if (flag == 1)
		rt *= -1;
	return (rt);
}
