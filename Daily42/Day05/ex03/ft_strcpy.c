char	*ft_strcpy(char *dest, char *src)
{
	int		count;

	count = 0;
	while (*src != '\0')
	{
		*dest = *src;
		dest++;
		src++;
		count++;
	}
	*dest = '\0';
	dest -= count;
	return (dest);
}
