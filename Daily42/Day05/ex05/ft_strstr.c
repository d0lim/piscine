char	*ft_strstr(char *str, char *to_find)
{
	char	*i;
	char	*j;

	j = to_find;
	if (*j == '\0')
		return (str);
	while (*str != '\0')
	{
		if (*str == *j)
		{
			i = str;
			while (*i == *j || *j == '\0')
			{
				if (*j == '\0')
					return (str);
				i++;
				j++;
			}
			j = to_find;
		}
		str++;
	}
	return (0);
}
