char	*ft_strrev(char *str)
{
	int		i;
	int		n;
	int		h;
	char	t;

	i = 0;
	h = 0;
	n = 0;
	while (str[i] != '\0')
	{
		i++;
		n++;
	}
	i -= 1;
	while (i >= n / 2)
	{
		t = str[i];
		str[i] = str[h];
		str[h] = t;
		h++;
		i--;
	}
	return (str);
}
