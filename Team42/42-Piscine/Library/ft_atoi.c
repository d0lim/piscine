int		ft_atoi(char *str)
{
	int i;
	int res;
	int sign;

	sign = 1;
	i = 0;
	res = 0;
	while (str[i] <= ' ')
		i++;
	if (str[i] == '-')
	{
		sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = (res * 10) + str[i] - '0';
		i++;
	}
	res *= sign;
	return (res);
}
